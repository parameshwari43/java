public class nestedstatement
{
    public static void main(String [] args)
    {
        int mark=90;
        System.out.println("Enter the mark:"+mark);
        if(mark<=100)
        {
            System.out.println("Grade:O");
        }
        else if(mark<=90)
        {
            System.out.println("Grade:A");
        }
        else if(mark<=80)
        {
            System.out.println("Grade:B");
        }
    }
}